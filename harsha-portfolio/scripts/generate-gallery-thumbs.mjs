#!/usr/bin/env node
// Pre-generate WebP thumbnails + inline base64 blur placeholders for the gallery.
// Run: cd harsha-portfolio && node scripts/generate-gallery-thumbs.mjs
// Output: public/gallery/<category>/<slug>-thumb.webp + src/data/galleryThumbs.generated.ts

import sharp from 'sharp'
import fs from 'fs/promises'
import path from 'path'
import { fileURLToPath } from 'url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const ROOT = path.resolve(__dirname, '..')
const GALLERY_DIR = path.join(ROOT, 'public', 'gallery')
const OUT_FILE = path.join(ROOT, 'src', 'data', 'galleryThumbs.generated.ts')

const THUMB_WIDTH = 900
const THUMB_QUALITY = 78
const BLUR_WIDTH = 12

const manifest = {}

async function processCategory(categoryName) {
  const categoryDir = path.join(GALLERY_DIR, categoryName)
  const files = await fs.readdir(categoryDir)
  let count = 0

  for (const file of files) {
    if (!file.endsWith('.png')) continue
    if (file.endsWith('-thumb.webp')) continue
    if (file.includes('-thumb')) continue

    const inputPath = path.join(categoryDir, file)
    const basename = path.basename(file, '.png')
    const thumbName = `${basename}-thumb.webp`
    const thumbPath = path.join(categoryDir, thumbName)

    // Generate main thumbnail
    await sharp(inputPath)
      .resize({ width: THUMB_WIDTH, withoutEnlargement: true })
      .webp({ quality: THUMB_QUALITY })
      .toFile(thumbPath)

    // Generate blur placeholder (tiny blurred WebP as base64 data URL)
    const blurBuffer = await sharp(inputPath)
      .resize({ width: BLUR_WIDTH })
      .blur(2)
      .webp({ quality: 50 })
      .toBuffer()
    const blurDataURL = `data:image/webp;base64,${blurBuffer.toString('base64')}`

    manifest[basename] = {
      thumbSrc: `/gallery/${categoryName}/${thumbName}`,
      blurDataURL,
    }

    const thumbStat = await fs.stat(thumbPath)
    console.log(`  ${basename}: ${Math.round(thumbStat.size / 1024)}KB webp + ${blurBuffer.length}B blur`)
    count++
  }

  return count
}

async function main() {
  const categories = await fs.readdir(GALLERY_DIR)
  let total = 0
  for (const cat of categories) {
    const statPath = path.join(GALLERY_DIR, cat)
    const stat = await fs.stat(statPath)
    if (!stat.isDirectory()) continue
    console.log(`\nProcessing ${cat}...`)
    total += await processCategory(cat)
  }

  const output = `// AUTO-GENERATED by scripts/generate-gallery-thumbs.mjs -- do not edit manually
// Run: npm run thumbs (from harsha-portfolio/) to regenerate after changing gallery assets

export interface GalleryThumb {
  thumbSrc: string
  blurDataURL: string
}

export const galleryThumbs: Record<string, GalleryThumb> = ${JSON.stringify(manifest, null, 2)}

export function getThumb(src: string): GalleryThumb | undefined {
  const key = src.split('/').pop()?.replace(/\\.(png|webp|jpg|jpeg)$/, '')
  return key ? galleryThumbs[key] : undefined
}
`

  await fs.mkdir(path.dirname(OUT_FILE), { recursive: true })
  await fs.writeFile(OUT_FILE, output, 'utf-8')
  console.log(`\n${total} thumbnails generated.`)
  console.log(`Manifest: ${path.relative(ROOT, OUT_FILE)}`)
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
